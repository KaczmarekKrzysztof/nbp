import Foundation
import UIKit

extension UIColor {
    
    static func sectionHeader() -> UIColor {
        return UIColor.orange
    }
    
    static func cellBackground() -> UIColor {
        return UIColor(red: 34/255, green: 34/255, blue: 34/255, alpha: 1)
    }
    
    static func background() -> UIColor {
        return UIColor(red: 17/255, green: 17/255, blue: 17/255, alpha: 1)
    }
    
}
